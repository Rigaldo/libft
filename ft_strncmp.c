#include "libft.h"

int		ft_strncmp(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i] && s1[i] && s2[i] && i < n)
		i++;
	if (i == n)
		return ((int)((unsigned char)s1[i - 1] - (unsigned char)s2[i - 1]));
	return ((int)((unsigned char)s1[i] - (unsigned char)s2[i]));
}
